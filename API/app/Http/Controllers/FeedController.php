    {
        $input = $request->all();
        $oFeed = new Feed;

        $oFeed->user_id = 1;
        $oFeed->text = $input['text'];
        if($oFeed->save()) {
            return Feed::where('id', $oFeed->id)->first();
        }

        return false;
    }

